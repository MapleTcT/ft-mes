/* TABLE: 规则表                                       */
/*====================================================*/
CREATE TABLE COUNTER_RULE  (
  ID             NUMBER(20,0) NOT NULL ,--COMMENT '规则ID',
  RULE_NAME      VARCHAR2(128) NOT NULL ,--COMMENT '规则名称',
  VALID          NUMBER(3,0) DEFAULT 0 ,--COMMENT '是否失效',
  CREATOR        VARCHAR2(32) DEFAULT 'SYSTEM' ,--COMMENT '创建者',
  CREATE_TIME    TIMESTAMP DEFAULT CURRENT_TIMESTAMP ,--COMMENT '创建时间',
  CREATE_STAFF_ID NUMBER(20,0) DEFAULT 0 ,--COMMENT '创建者ID',
  MODIFIER       VARCHAR2(32) ,--COMMENT '修改人',
  MODIFY_TIME    TIMESTAMP ,--COMMENT '修改时间',
  MODIFY_STAFF_ID NUMBER(20,0) ,--COMMENT '修改者ID',
  PRIMARY KEY (ID)
);
COMMENT ON TABLE COUNTER_RULE IS '规则表';
COMMENT ON COLUMN COUNTER_RULE.ID IS '规则ID';
COMMENT ON COLUMN COUNTER_RULE.RULE_NAME IS '规则名称';
COMMENT ON COLUMN COUNTER_RULE.VALID IS '是否失效';
COMMENT ON COLUMN COUNTER_RULE.CREATOR IS '创建者';
COMMENT ON COLUMN COUNTER_RULE.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN COUNTER_RULE.CREATE_STAFF_ID IS '创建者ID';
COMMENT ON COLUMN COUNTER_RULE.MODIFIER IS '修改人';
COMMENT ON COLUMN COUNTER_RULE.MODIFY_TIME IS '修改时间';
COMMENT ON COLUMN COUNTER_RULE.MODIFY_STAFF_ID IS '修改者ID';



/*====================================================*/
/* TABLE: 规则子表                                     */
/*====================================================*/
 CREATE TABLE COUNTER_RULE_FIELD  (
   ID             NUMBER(20,0) NOT NULL ,--COMMENT '字段ID',
