   AUTO_DATE_RULE NUMBER(3,0) DEFAULT 0 ,--COMMENT '按日期自增规则：0=DAILY=按天 1=MONTHLY=按月 2=YEARLY=按年',
   FIELD_ORDER    NUMBER(10,0) DEFAULT 1 ,--COMMENT '字段排序码：>=1',
