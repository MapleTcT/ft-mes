   FIELD_TYPE     NUMBER(3,0) NOT NULL ,--COMMENT '字段类型：0=DATE=日期 1=AUTO=自增 2=CUSTOM=自定义 3=PROPERTY=实体字段 4=INHERENT=实体属性 5=SEPARATOR=分隔符',
   THECASE        NUMBER(3,0) DEFAULT 0 ,--COMMENT '大小写样式：0=ORIGINAL=保持原样 1=UPPER=大写 2=LOWER=小写',
